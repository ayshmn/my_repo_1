# Integer (int) number type

a = 1
b = 123456789
c = -123456789

print(type(a))
print(type(b))
print(type(c))


# Float (float) number type

a = 0.1
b = 1.23456789
c = -1.23456789
d = 35e3
e = 12E4
f = -1.23e45


print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(type(f))


# Complex (complex) number type

a = 3+5j
b = 5j
c = -5j
d = -2j+6

print(type(a))
print(type(b))
print(type(c))
print(type(d))





